@router.get("/me", response_model=UserResponse)
def get_current_user_info(current_user: User = Depends(get_current_active_user)):
    """Get current user information"""
    return current_user


@router.get("/modes")
def get_user_modes(current_user: User = Depends(get_current_active_user)):
    """Get available modes for current user"""
    return {"modes": current_user.modes}
